def get_api_key() -> str:
    """
    Get API key from Windows System Environment Variables.
    
    Returns:
        str: The API key string
        
    Raises:
        ValueError: If API key is not set in environment variables
    """
    api_key = os.environ.get('PARTNER_CLUB_API_KEY')
    if not api_key:
        logger.error("API key not found in environment variables")
        raise ValueError("API key 'PARTNER_CLUB_API_KEY' not found in environment variables")
    return api_key

def validate_plan_parameters(parameters: Dict[str, Any]) -> bool:
    """
    Validate that plan parameters are complete and well-formed.
    
    Args:
        parameters (Dict[str, Any]): Plan parameters to validate
        
    Returns:
        bool: True if valid, False otherwise
        
    Raises:
        ValueError: If parameters are invalid or missing required fields
    """
    required_keys = [
        'max_investor_count', 'scheduled_payment', 'min_payment_new', 
        'min_payment_re', 'revenue_base_divisor', 'sales_commission', 
        'settlement_bonus', 'max_bonus', 'round_bonus_rates', 
        'sales_achievement_rates'
    ]
    
    missing_keys = [key for key in required_keys if key not in parameters]
    if missing_keys:
        logger.error(f"Missing required parameters: {', '.join(missing_keys)}")
        raise ValueError(f"Missing required parameters: {', '.join(missing_keys)}")
    
    return True
