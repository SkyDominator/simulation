def get_plan_parameters(plan: str) -> Dict[str, Any]:
    """
    Get the parameters for a specific plan.
    
    Args:
        plan (str): The plan identifier (e.g., 'A', 'B', 'C', etc.)
        
    Returns:
        Dict[str, Any]: Dictionary containing plan parameters
        
    Raises:
        ValueError: If the plan is not supported
    """
    plans = _get_all_plan_parameters()
    
    if plan not in plans:
        logger.error(f"Unsupported plan: {plan}")
        raise ValueError(f"Unsupported plan: {plan}")
    
    logger.info(f"Successfully loaded parameters for plan {plan}")
    return plans[plan]

